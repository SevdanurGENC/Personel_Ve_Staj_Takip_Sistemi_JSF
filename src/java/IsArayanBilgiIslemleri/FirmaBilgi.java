public class FirmaBilgi {
    
    private String firma_adi;
    private String sehir;
    private String web;
    private String sektor;
    private String isalani;

    public FirmaBilgi() {
    }

    public String getFirma_adi() {
        return firma_adi;
    }

    public void setFirma_adi(String firma_adi) {
        this.firma_adi = firma_adi;
    }

    public String getIsalani() {
        return isalani;
    }

    public void setIsalani(String isalani) {
        this.isalani = isalani;
    }

    public String getSehir() {
        return sehir;
    }

    public void setSehir(String sehir) {
        this.sehir = sehir;
    }

    public String getSektor() {
        return sektor;
    }

    public void setSektor(String sektor) {
        this.sektor = sektor;
    }

    public String getWeb() {
        return web;
    }

    public void setWeb(String web) {
        this.web = web;
    }
    
    
    
}
